/*
 * Что такое ЧПУ
 * ЧПУ - веб-адрес, удобный для восприятия человека.
 * Аббревиатура от "Человеку Понятный УРЛ (URL)"
 * URL (Uniform Resource Locator) - (Единый Указатель Ресурса)
 *
 * примеры не ЧПУ
 * ---------------------------------------------------------------------------------------
 * http://example.com/user.php?user_id=15
 * http://example.com/user.php?page=consulting/marketing
 * http://example.com/user.php?product_id=77
 *
 * примеры ЧПУ
 * ---------------------------------------------------------------------------------------
 * http://example.com/user/525
 * http://example.com/consulting/marketing
 * http://example.com/product/25
 *
 * Когда название состоит из нескольких слов:
 * http://example.com/consulting/strategy-games
 * http://example.com/consulting/strategy_games
 *
 * Такой прием иногда называют Slug - часть URL адреса, в котором в качестве
 * идентификатора страницы используются слова, понятные человеку
 * http://example.com/aram-aramyan
 * http://example.com/user/5255
